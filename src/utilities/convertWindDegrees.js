export function convertWindDegrees(degrees) {

    const cardinalPoints = [
        ["N", [0.00, 11.25]],
        ["NbE", [11.25, 22.50]],
        ["NNE", [22.50, 33.75]],
        ["NEbN", [33.75, 45.00]],
        ["NE", [45.00, 56.25]],
        ["NEbE", [56.25, 67.50]],
        ["ENE", [67.50, 78.75]],
        ["EbN", [78.75, 90.00]],
        ["E", [90.00, 101.25]],
        ["EbS", [101.25, 112.50]],
        ["ESE", [112.50, 123.75]],
        ["SEbE", [123.75, 135.00]],
        ["SE", [135.00, 146.25]],
        ["SEbS", [146.25, 157.50]],
        ["SSE", [157.50, 168.75]],
        ["SbE", [168.75, 180.00]],
        ["S", [180.00, 191.25]],
        ["SbW", [191.25, 202.50]],
        ["SSW", [202.50, 213.75]],
        ["SWbS", [213.75, 225.00]],
        ["SW", [225.00, 236.25]],
        ["SWbW", [236.25, 247.50]],
        ["WSW", [247.50, 258.75]],
        ["WbS", [258.75, 270.00]],
        ["W", [270.00, 281.25]],
        ["WbN", [281.25, 292.50]],
        ["WNW", [292.50, 303.75]],
        ["NWbW", [303.75, 315.00]],
        ["NW", [315.00, 326.25]],
        ["NWbN", [326.25, 337.50]],
        ["NNW", [337.50, 348.75]],
        ["NbW", [348.75, 360.00]]
    ];

    for (const [abbreviation, [lower, upper]] of cardinalPoints) {
        if (degrees >= lower && degrees < upper) {
            return abbreviation;
        }
    }

    return "Invalid Degree";
}



